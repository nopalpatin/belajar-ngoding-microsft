#operasi komparasi

a = 10
b = 20

#operator == (sama dengan)
print(a == b)

#operator != (tidak sama dengan)
print(a != b)

#operator > (lebih besar dari)
print(a > b)

#operator < (lebih kecil dari)
print(a < b)

#operator >= (lebih besar sama dengan)
print(a >= b)

#operator <= (lebih kecil sama dengan)
print(a <= b)

#operator and (dan)
print(a == b and a < b)

#operator or (atau)
print(a == b or a < b)

#operator not (tidak)
print(not a == b)

#operator is (sama dengan)
print(a is b)
